package pkg;

public class SwitchExample {

	public static void main(String[] args) {
		
		 char penaltiKick = 'F';
		 
		 switch(penaltiKick){
		 
		 case 'F':
		     System.out.println("Messi is in Forward Half Position");
		 
		 break;
		 
		 case 'C':
		     System.out.println("Messi is as a Centre Half");
		 break;
		 
		 case 'B':
		     System.out.println("Messi is playing at Back");
		 break;
		 
		 default:
		     System.out.println("Messi is in Golie Position");
		     
		     
		 }
	}
	
}
